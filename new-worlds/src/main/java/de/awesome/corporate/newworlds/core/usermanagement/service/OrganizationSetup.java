package de.awesome.corporate.newworlds.core.usermanagement.service;

public class OrganizationSetup {

	private int capsules;
	private int cattle;
	private int crops;
	private int industry;

	private int population;

	public int getCapsules() {
		return capsules;
	}

	public int getCattle() {
		return cattle;
	}

	public int getCrops() {
		return crops;
	}

	public int getIndustry() {
		return industry;
	}

	public int getPopulation() {
		return population;
	}

	public void setCapsules(int capsules) {
		this.capsules = capsules;
	}

	public void setCattle(int cattle) {
		this.cattle = cattle;
	}

	public void setCrops(int crops) {
		this.crops = crops;
	}

	public void setIndustry(int industry) {
		this.industry = industry;
	}

	public void setPopulation(int population) {
		this.population = population;
	}

}
